function repoHygieneJob(selected) {
  const needsGoInstall = selected.has('github-actions') || selected.has('bash');
  const steps = ['      - name: Check out repository\n        uses: actions/checkout@v6'];

  if (needsGoInstall) {
    steps.push(`      - name: Set up Go for lint tools
        uses: actions/setup-go@v6
        with:
          go-version: stable`);
  }

  const installLines = [];
  if (selected.has('github-actions')) {
    installLines.push('go install github.com/rhysd/actionlint/cmd/actionlint@latest');
  }
  if (selected.has('bash')) {
    installLines.push('sudo apt-get update');
    installLines.push('sudo apt-get install -y shellcheck');
    installLines.push('go install mvdan.cc/sh/v3/cmd/shfmt@latest');
  }
  if (installLines.length > 0) {
    steps.push(`      - name: Install repository lint tools
        run: |
          ${installLines.join('\n          ')}
          echo "$(go env GOPATH)/bin" >> "$GITHUB_PATH"`);
  }

  if (selected.has('github-actions')) {
    steps.push(step('Lint GitHub Actions workflows', 'actionlint'));
  }
  if (selected.has('bash')) {
    steps.push(`      - name: Check shell scripts
        run: |
          mapfile -t shell_files < <(find . -type f \\( -name '*.sh' -o -name '*.bash' \\) -not -path './.git/*' -not -path './node_modules/*' -not -path './dist/*' -not -path './build/*' -not -path './out/*' -not -path './target/*')
          if [ "\${#shell_files[@]}" -eq 0 ]; then
            echo "No shell scripts found; skipping shell checks."
            exit 0
          fi

          shfmt -d -i 2 -ci -sr "\${shell_files[@]}"
          shellcheck "\${shell_files[@]}"
          for file in "\${shell_files[@]}"; do
            bash -n "$file"
          done`);
  }
  if (selected.has('docker')) {
    steps.push(`      - name: Check Dockerfiles
        run: |
          mapfile -t dockerfiles < <(find . -type f \\( -name 'Dockerfile' -o -name 'Dockerfile.*' \\) -not -path './.git/*' -not -path './node_modules/*' -not -path './dist/*' -not -path './build/*' -not -path './out/*' -not -path './target/*')
          if [ "\${#dockerfiles[@]}" -eq 0 ]; then
            echo "No Dockerfiles found; skipping Docker checks."
            exit 0
          fi

          docker run --rm -v "$PWD:/workspace" -w /workspace hadolint/hadolint:latest "\${dockerfiles[@]}"
          for dockerfile in "\${dockerfiles[@]}"; do
            docker buildx build --check -f "$dockerfile" "$(dirname "$dockerfile")"
          done`);
  }

  return `  repo-hygiene:
    name: Repository hygiene
    runs-on: ubuntu-latest
    steps:
${steps.join('\n')}`;
}

