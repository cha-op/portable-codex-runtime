function benchmarkScript(selected) {
  const sections = [];

  if (selected.has('js-ts')) {
    sections.push(`if [ -f package.json ]; then
  require_command pnpm
  run_step "JavaScript/TypeScript benchmarks" pnpm run bench
else
  echo "Skipping JavaScript/TypeScript benchmarks: package.json not found."
fi`);
  }

  if (selected.has('python')) {
    sections.push(`if [ -f pyproject.toml ]; then
  require_command uv
  run_step "Python benchmarks" uv run --group benchmark pytest --benchmark-only
else
  echo "Skipping Python benchmarks: pyproject.toml not found."
fi`);
  }

  if (selected.has('go')) {
    sections.push(`if [ -f go.mod ]; then
  require_command go
  run_step "Go benchmarks" go test -bench . -benchmem ./...
else
  echo "Skipping Go benchmarks: go.mod not found."
fi`);
  }

  if (selected.has('rust')) {
    sections.push(`if [ -f Cargo.toml ]; then
  require_command cargo
  run_step "Rust benchmarks" cargo bench
else
  echo "Skipping Rust benchmarks: Cargo.toml not found."
fi`);
  }

  return `#!/usr/bin/env bash
# ${GENERATED_BY}
set -euo pipefail

ran=0

require_command() {
  if ! command -v "$1" >/dev/null 2>&1; then
    echo "Missing required command: $1" >&2
    exit 127
  fi
}

run_step() {
  local label="$1"
  shift
  printf '\\n==> %s\\n' "$label"
  "$@"
  ran=1
}

${sections.join('\n\n')}

if [ "$ran" -eq 0 ]; then
  echo "No benchmark commands were run for the selected modules."
fi
`;
}

function isDirectRun() {
  if (!process.argv[1]) {
    return false;
  }

  try {
    return (
      realpathSync.native(fileURLToPath(import.meta.url)) ===
      realpathSync.native(path.resolve(process.argv[1]))
    );
  } catch (error) {
    if (error?.code === 'ENOENT') {
      return false;
    }
    throw error;
  }
}

if (isDirectRun()) {
  const status = await runCli();
  process.exitCode = status;
}
